package day5_May12th;

public class Area {
	void areac (float r)
	{
		System.out.println("Area of circle is "+ (2*3.14f*r));
	}
	void areac (int l, int b)
	{
		System.out.println("Area of rectangle is "+ (l*b));
	}
void areac (int s)
{
	System.out.println("Area of square is "+ (s*s));
}
void areac (int base, float h)
{
	System.out.println("Area of triangle is "+ (0.5f*base*h));
}

}
